    private void brnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnActionPerformed
        int n1 = Integer.parseInt(txtn1.getText()), n2 = Integer.parseInt(txtn2.getText());
        String tipoConta = (String) conta.getSelectedItem()  ;
        float res;
        if(null == tipoConta){
            res = n1*n2;
        } else switch (tipoConta) {
            case "Soma":
                res = n1+ n2;
                break;
            case "Subtração":
                res = n1- n2;
                break;
            case "Divisão":
                res = n1/ n2;
                break;
            default:
                res = n1*n2;
                break;
        }
        
        resultado.setText(Float.toString(res));
        contaName.setText(tipoConta);

    }//GEN-LAST:event_brnActionPerformed

