    private void contaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_contaItemStateChanged
        System.out.println("In StatteChanged");
        
        String tipoConta = (String) conta.getSelectedItem();
        contaName.setText(tipoConta);
        switch (tipoConta) {
            case "Soma":
                contaSimbolo.setText("+");
                break;
            case "Subtração":
                contaSimbolo.setText("-");
                break;
            case "Divisão":
                contaSimbolo.setText(":");
                break;
            default:
                contaSimbolo.setText("x");
                break;
        }
    }//GEN-LAST:event_contaItemStateChanged

